package com.tcaulk.smash.animation;

public class AnimationFrame {

    public final int x;
    public final int y;
    public final int width;
    public final int height;

    public AnimationFrame(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}